// 헬퍼 함수들
export function getPostById(id: number): Post | undefined {
  return posts.find((p) => p.id === id);
}

export function getAnalysisPosts(): Post[] {
  return posts.filter((p) => p.category === "analysis");
}

export function getNewsPosts(): Post[] {
  return posts.filter((p) => p.category === "news");
}

export function getRecentPosts(excludeId?: number, limit = 3): Post[] {
  return posts
    .filter((p) => p.id !== excludeId)
    .sort((a, b) => b.views - a.views)
    .slice(0, limit);
}

export function getAdjacentPosts(id: number) {
  const idx = posts.findIndex((p) => p.id === id);
  return {
    prev: idx > 0 ? posts[idx - 1] : null,
    next: idx < posts.length - 1 ? posts[idx + 1] : null,
  };
}
